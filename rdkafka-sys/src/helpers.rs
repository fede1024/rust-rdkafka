#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_conversion() {
        for error_code in -299..300 {
            if let Some(resp_err) = primitive_to_rd_kafka_resp_err_t(error_code) {
                let kafka_error = rd_kafka_resp_err_t_to_rdkafka_error(resp_err);
                assert_eq!(error_code, kafka_error as i32);
            }
        }
    }
}
